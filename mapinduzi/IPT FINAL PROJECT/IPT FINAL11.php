<script>
    let cart = [];

    function addToCart(serviceName, servicePrice, button) {
        cart.push({ name: serviceName, price: servicePrice, button: button });
        updateCartSummary();
        document.getElementById('cart-summary').style.display = 'block';
        button.disabled = false;
        document.getElementById('book-now').disabled = false;;
    }

    function updateCartSummary() {
        const cartTable = document.getElementById('cart-table');
        cartTable.innerHTML = '';
        cart.forEach((item, index) => {
            const row = cartTable.insertRow();
            const cell1 = row.insertCell(0);
            const cell2 = row.insertCell(1);
            const cell3 = row.insertCell(2);
            cell1.innerHTML = item.name;
            cell2.innerHTML = 'Tsh' + item.price;
            cell3.innerHTML = `<button class="remove-button" onclick="removeFromCart(${index})">X</button>`;
        });
        document.getElementById('cart-count').innerText = 'Number of services ordered: ' + cart.length;
        const total = cart.reduce((sum, item) => sum + item.price, 0);
        document.getElementById('total-price').innerText = 'Total Price Tsh' + total;
    }

    function removeFromCart(index) {
        const item = cart.splice(index, 1)[0];
        item.button.disabled = false;
        updateCartSummary();
        if (cart.length === 0) {
            document.getElementById('cart-summary').style.display = 'none';
            document.getElementById('book-now').disabled = true;
        }
    }

    function closeCartSummary() {
        document.getElementById('cart-summary').style.display = 'none';
    }

    function bookNow() {
        alert('Dear costomer please register for booking!');
        location.reload();
    }

    document.getElementById("contactBtn").addEventListener("click", function () {
            var formContainer = document.getElementById("contactFormContainer");
            formContainer.style.display = formContainer.style.display === "block" ? "none" : "block";
        });

        document.getElementById("contactForm").addEventListener("submit", function (event) {
            event.preventDefault();
            alert("Form submitted successfully!");
        });

